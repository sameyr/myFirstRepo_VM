#include <stdio.h>
int main()
{
	float number1 = 13.5;
	double number2 = 12.4;
	char chr ='a';
	
	printf("Number1 = %f\n", number1); // %d is format specefier which act as a placeholder
					      // '\n' changes line in C as any other programming language
	printf("Number2 = %d\n", number2);
	printf("Character = %c\n", chr);

	return 0;
}

